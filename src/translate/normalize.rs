/// Remove universal quantifiers from all conditions in the task.
///
/// This implements step [1] of normalization: for each action precondition,
/// axiom condition, effect condition, and the goal, replace forall quantifiers
/// with axioms.
pub fn task_remove_universal_quantifiers(task: &mut NormalizableTask) {
    let mut axiom_cache: HashMap<String, String> = HashMap::new();
    // Process action preconditions
    for i in 0..task.actions.len() {
        if task.actions[i].precondition.has_universal_part() {
            let type_map = NormalizableTask::build_type_map(&task.actions[i].parameters);
            let condition = task.actions[i].precondition.clone();
            let new_condition = remove_universal_quantifiers_impl(
                &condition,
                &type_map,
                task,
                &mut axiom_cache,
            );
            task.actions[i].precondition = new_condition;
        }
    }
    
    // Process axiom conditions
    let mut i = 0;
    while i < task.axioms.len() {
        if task.axioms[i].condition.has_universal_part() {
            let type_map = NormalizableTask::build_type_map(&task.axioms[i].parameters);
            let condition = task.axioms[i].condition.clone();
            let new_condition = remove_universal_quantifiers_impl(
                &condition,
                &type_map,
                task,
                &mut axiom_cache,
            );
            task.axioms[i].condition = new_condition;
        }
        i += 1;
    }
    
    // Process effect conditions
    for i in 0..task.actions.len() {
        for j in 0..task.actions[i].effects.len() {
            if task.actions[i].effects[j].condition.has_universal_part() {
                let mut combined_params = task.actions[i].parameters.clone();
                combined_params.extend(task.actions[i].effects[j].parameters.clone());
                let type_map = NormalizableTask::build_type_map(&combined_params);
                let condition = task.actions[i].effects[j].condition.clone();
                let new_condition = remove_universal_quantifiers_impl(
                    &condition,
                    &type_map,
                    task,
                    &mut axiom_cache,
                );
                task.actions[i].effects[j].condition = new_condition;
            }
        }
    }
    
    // Process goal
    if task.goal.has_universal_part() {
        let type_map = HashMap::new(); // Goal has no parameters typically
        let goal = task.goal.clone();
        task.goal = remove_universal_quantifiers_impl(&goal, &type_map, task, &mut axiom_cache);
    }
}

/// Helper function for remove_universal_quantifiers that recursively processes a condition
fn remove_universal_quantifiers_impl(
    condition: &Condition,
    type_map: &HashMap<String, String>,
    task: &mut NormalizableTask,
    axiom_cache: &mut HashMap<String, String>,
) -> Condition {
    match condition {
        Condition::Forall(_params, inner) => {
            // Create axiom for negation of the forall
            let axiom_condition = condition.negate();
            let cache_key = condition_key(&axiom_condition);
            if let Some(existing_name) = axiom_cache.get(&cache_key) {
                let mut free_vars: Vec<_> = axiom_condition.free_variables().into_iter().collect();
                free_vars.sort();
                return Condition::Not(Box::new(Condition::Atom(existing_name.clone(), free_vars)));
            }
            let free_vars: Vec<_> = axiom_condition.free_variables().into_iter().collect();
            let mut axiom_params: Vec<(String, Option<String>)> = free_vars
                .iter()
                .map(|v| (v.clone(), type_map.get(v).cloned()))
                .collect();
            axiom_params.sort_by(|a, b| a.0.cmp(&b.0));

            let axiom_name = task.add_axiom(axiom_params.clone(), axiom_condition.clone());
            axiom_cache.insert(cache_key, axiom_name.clone());
            let axiom_args: Vec<String> = axiom_params.iter().map(|(n, _)| n.clone()).collect();
            Condition::Not(Box::new(Condition::Atom(axiom_name, axiom_args)))
        }
        Condition::Not(c) => {
            Condition::Not(Box::new(remove_universal_quantifiers_impl(
                c,
                type_map,
                task,
                axiom_cache,
            )))
        }
        Condition::And(parts) => {
            let new_parts: Vec<_> = parts
                .iter()
                .map(|p| remove_universal_quantifiers_impl(p, type_map, task, axiom_cache))
                .collect();
            Condition::And(new_parts)
        }
        Condition::Or(parts) => {
            let new_parts: Vec<_> = parts
                .iter()
                .map(|p| remove_universal_quantifiers_impl(p, type_map, task, axiom_cache))
                .collect();
            Condition::Or(new_parts)
        }
        Condition::Exists(params, c) => {
            let new_inner = remove_universal_quantifiers_impl(c, type_map, task, axiom_cache);
            Condition::Exists(params.clone(), Box::new(new_inner))
        }
        _ => condition.clone(),
    }
}

fn condition_key(condition: &Condition) -> String {
    match condition {
        Condition::Atom(name, args) => format!("atom({} {})", name, args.join(" ")),
        Condition::Not(inner) => format!("not({})", condition_key(inner)),
        Condition::And(parts) => {
            let parts_key: Vec<String> = parts.iter().map(condition_key).collect();
            format!("and({})", parts_key.join(","))
        }
        Condition::Or(parts) => {
            let parts_key: Vec<String> = parts.iter().map(condition_key).collect();
            format!("or({})", parts_key.join(","))
        }
        Condition::Forall(params, inner) => {
            let params_key: Vec<String> = params
                .iter()
                .map(|(n, t)| format!("{}:{}", n, t.clone().unwrap_or_default()))
                .collect();
            format!("forall({}){}", params_key.join(","), condition_key(inner))
        }
        Condition::Exists(params, inner) => {
            let params_key: Vec<String> = params
                .iter()
                .map(|(n, t)| format!("{}:{}", n, t.clone().unwrap_or_default()))
                .collect();
            format!("exists({}){}", params_key.join(","), condition_key(inner))
        }
        Condition::Comparison(op, left, right) => {
            format!("cmp({} {} {})", op, sexpr_key(left), sexpr_key(right))
        }
        Condition::True => "true".to_string(),
    }
}

fn sexpr_key(sexpr: &SExpr) -> String {
    match sexpr {
        SExpr::Atom(a) => a.clone(),
        SExpr::List(items) => {
            let parts: Vec<String> = items.iter().map(sexpr_key).collect();
            format!("({})", parts.join(" "))
        }
    }
}

/// Substitute complicated goal with an axiom reference.
///
/// This implements step [2] of normalization for the goal specifically.
pub fn task_substitute_complicated_goal(task: &mut NormalizableTask) {
    let goal = task.goal.clone();
    task.goal = substitute_complicated_goal_impl(&goal, task);
}

/// Helper that can add axioms to task
fn substitute_complicated_goal_impl(goal: &Condition, task: &mut NormalizableTask) -> Condition {
    // Check if goal is a simple literal or conjunction of literals
    match goal {
        Condition::Atom(_, _) => return goal.clone(),
        Condition::Not(inner) => {
            if matches!(**inner, Condition::Atom(_, _)) {
                return goal.clone();
            }
        }
        Condition::And(parts) => {
            let all_literals = parts.iter().all(|p| match p {
                Condition::Atom(_, _) => true,
                Condition::Not(inner) => matches!(**inner, Condition::Atom(_, _)),
                _ => false,
            });
            if all_literals {
                return goal.clone();
            }
        }
        _ => {}
    }
    
    // Goal is complicated - create axiom
    let axiom_name = task.add_axiom(vec![], goal.clone());
    Condition::Atom(axiom_name, vec![])
}

/// Convert all conditions in the task to DNF.
///
/// This implements step [3] of normalization.
pub fn task_build_dnf(task: &mut NormalizableTask) {
    // Process action preconditions
    for action in task.actions.iter_mut() {
        if action.precondition.has_disjunction() {
            action.precondition = simplify_condition(&build_dnf(&action.precondition));
        }
    }
    
    // Process axiom conditions
    for axiom in task.axioms.iter_mut() {
        if axiom.condition.has_disjunction() {
            axiom.condition = simplify_condition(&build_dnf(&axiom.condition));
        }
    }
    
    // Process effect conditions
    for action in task.actions.iter_mut() {
        for effect in action.effects.iter_mut() {
            if effect.condition.has_disjunction() {
                effect.condition = simplify_condition(&build_dnf(&effect.condition));
            }
        }
    }
    
    // Process goal
    if task.goal.has_disjunction() {
        task.goal = simplify_condition(&build_dnf(&task.goal));
    }
}

/// Split disjunctions in the task, creating multiple copies of actions/axioms.
///
/// This implements step [4] of normalization.
pub fn task_split_disjunctions(task: &mut NormalizableTask) {
    // Split action preconditions
    let mut new_actions = Vec::new();
    let mut actions_to_remove = Vec::new();
    
    for (idx, action) in task.actions.iter().enumerate() {
        if let Some(parts) = split_disjunction(&action.precondition) {
            // Create a copy of the action for each disjunct
            for part in parts {
                let mut new_action = action.clone();
                new_action.precondition = part;
                new_actions.push(new_action);
            }
            actions_to_remove.push(idx);
        }
    }
    
    // Remove original actions (in reverse order to maintain indices)
    for idx in actions_to_remove.iter().rev() {
        task.actions.remove(*idx);
    }
    task.actions.extend(new_actions);
    
    // Split axiom conditions
    let mut new_axioms = Vec::new();
    let mut axioms_to_remove = Vec::new();
    
    for (idx, axiom) in task.axioms.iter().enumerate() {
        if let Some(parts) = split_disjunction(&axiom.condition) {
            for part in parts {
                let mut new_axiom = axiom.clone();
                new_axiom.condition = part;
                new_axioms.push(new_axiom);
            }
            axioms_to_remove.push(idx);
        }
    }
    
    for idx in axioms_to_remove.iter().rev() {
        task.axioms.remove(*idx);
    }
    task.axioms.extend(new_axioms);

    // Split conditional effect conditions
    for action in task.actions.iter_mut() {
        let mut new_effects = Vec::new();
        for effect in action.effects.iter() {
            if let Some(parts) = split_disjunction(&effect.condition) {
                for part in parts {
                    let mut new_effect = effect.clone();
                    new_effect.condition = part;
                    new_effects.push(new_effect);
                }
            } else {
                new_effects.push(effect.clone());
            }
        }
        action.effects = new_effects;
    }
    
    // Note: Goal disjunctions should have been converted to axioms in substitute_complicated_goal
}

/// Move existential quantifiers in all conditions.
///
/// This implements step [5] of normalization.
pub fn task_move_existential_quantifiers(task: &mut NormalizableTask) {
    // Process action preconditions
    for action in task.actions.iter_mut() {
        if action.precondition.has_existential_part() {
            action.precondition = move_existential_quantifiers(&action.precondition);
        }
    }
    
    // Process axiom conditions
    for axiom in task.axioms.iter_mut() {
        if axiom.condition.has_existential_part() {
            axiom.condition = move_existential_quantifiers(&axiom.condition);
        }
    }
    
    // Process effect conditions
    for action in task.actions.iter_mut() {
        for effect in action.effects.iter_mut() {
            if effect.condition.has_existential_part() {
                effect.condition = move_existential_quantifiers(&effect.condition);
            }
        }
    }
    
    // Process goal
    if task.goal.has_existential_part() {
        task.goal = move_existential_quantifiers(&task.goal);
    }
}

/// Eliminate existential quantifiers from axioms (step 5a).
pub fn task_eliminate_existential_quantifiers_from_axioms(task: &mut NormalizableTask) {
    for axiom in task.axioms.iter_mut() {
        if let Condition::Exists(params, inner) = &axiom.condition {
            axiom.parameters.extend(params.clone());
            axiom.condition = (**inner).clone();
        }
    }
}

/// Eliminate existential quantifiers from action preconditions (step 5b).
pub fn task_eliminate_existential_quantifiers_from_preconditions(task: &mut NormalizableTask) {
    for action in task.actions.iter_mut() {
        if let Condition::Exists(params, inner) = &action.precondition {
            action.parameters.extend(params.clone());
            action.precondition = (**inner).clone();
        }
    }
}

/// Eliminate existential quantifiers from effect conditions (step 5c).
pub fn task_eliminate_existential_quantifiers_from_conditional_effects(task: &mut NormalizableTask) {
    for action in task.actions.iter_mut() {
        for effect in action.effects.iter_mut() {
            if let Condition::Exists(params, inner) = &effect.condition {
                effect.parameters.extend(params.clone());
                effect.condition = (**inner).clone();
            }
        }
    }
}

/// Main normalization function that applies all normalization steps.
///
/// This matches the Python normalize() function and applies the following steps:
/// 1. Remove universal quantifiers
/// 2. Substitute complicated goal
/// 3. Build DNF
/// 4. Split disjunctions
/// 5. Move existential quantifiers
/// 6. Eliminate existential quantifiers from axioms (5a)
/// 7. Eliminate existential quantifiers from preconditions (5b)
/// 8. Eliminate existential quantifiers from conditional effects (5c)
/// 9. Verify and fix arithmetic expressions
/// 10. Remove arithmetic expressions
/// 11. Verify axiom predicates
///
/// Returns an error if validation fails (e.g., derived predicates in :init or effects).
pub fn normalize(task: &mut NormalizableTask) -> Result<(), String> {
    task_remove_universal_quantifiers(task);
    task_substitute_complicated_goal(task);
    task_build_dnf(task);
    task_split_disjunctions(task);
    task_move_existential_quantifiers(task);
    task_eliminate_existential_quantifiers_from_axioms(task);
    task_eliminate_existential_quantifiers_from_preconditions(task);
    task_eliminate_existential_quantifiers_from_conditional_effects(task);
    task_verify_and_fix_arithmetic_expressions(task);
    task_remove_arithmetic_expressions(task);
    task_verify_axiom_predicates(task)?;
    Ok(())
}

/// Build exploration rules from normalized task for instantiation.
/// This converts normalized actions and axioms into datalog-style rules for model computation.
/// Mirrors Python's normalize.build_exploration_rules().
pub fn build_exploration_rules(
    task: &NormalizableTask,
) -> Result<Vec<(Vec<bm::SymAtom>, bm::SymAtom)>, String> {
    let mut rules = Vec::new();
    
    // For each action, create a rule for the precondition
    // @action-name(?params) :- precondition-atoms
    for action in &task.actions {
        let rule_head = get_action_predicate(action);
        let rule_body = condition_to_rule_body(&action.parameters, &action.precondition);
        rules.push((rule_body, rule_head));
        
        // Also create rules for effects (both unconditional and conditional)
        // effect-atom :- @action-name(?params), effect-condition
        for effect in &action.effects {
            // Get all variables: action parameters + effect forall parameters
            let mut all_params = action.parameters.clone();
            all_params.extend(effect.parameters.clone());
            
            let effect_heads = get_effect_rule_heads_checked(effect)?;
            
            if !effect_heads.is_empty() {
                let mut effect_body = vec![get_action_predicate(action)];
                // Add effect condition atoms (if not trivially true)
                if !matches!(&effect.condition, Condition::True) {
                    let cond_atoms = condition_to_rule_body(&[], &effect.condition);
                    effect_body.extend(cond_atoms);
                }
                for head in effect_heads {
                    rules.push((effect_body.clone(), head));
                }
            }
        }
    }
    
    // For each axiom, create rules
    for axiom in &task.axioms {
        // Application rule: @axiom(?params) :- axiom-condition
        let app_rule_head = get_axiom_predicate(axiom);
        let app_rule_body = condition_to_rule_body(&axiom.parameters, &axiom.condition);
        rules.push((app_rule_body, app_rule_head.clone()));
        
        // Effect rule: axiom-name(?external-params) :- @axiom(?params)
        // Python: params = axiom.parameters[:axiom.num_external_parameters]
        // For now, use all parameters (proper handling needs num_external_parameters)
        let eff_rule_head = bm::SymAtom::new(
            axiom.name.clone(),
            axiom
                .parameters
                .iter()
                .take(axiom.num_external_parameters)
                .map(|(name, _)| {
                    if name.starts_with('?') { name.clone() } else { format!("?{}", name) }
                })
                .collect(),
        );
        let eff_rule_body = vec![app_rule_head];
        rules.push((eff_rule_body, eff_rule_head));
    }
    
    // Goal reachability rule: @goal-reachable :- goal-atoms
    let goal_rule_head = bm::SymAtom::new("@goal-reachable".to_string(), vec![]);
    let goal_rule_body = condition_to_rule_body(&[], &task.goal);
    rules.push((goal_rule_body, goal_rule_head));
    
    // Add numeric axiom rules for function tracking (matches Python)
    // For each NumericAxiom from the function administrator:
    // 1. Rule: @axiom(?params) :- defined!part1(?args), defined!part2(?args), ...
    // 2. Rule: defined!axiom-head(?params) :- @axiom(?params)
    // 3. Rule: fluent-head(?params) :- @axiom(?params), fluent!part(?args)  (for fluent tracking)
    for axiom in &task.numeric_axioms {
        // Rule 1: @axiom :- defined!parts...
        // Head: atom representing the axiom application
        let axiom_pred = get_numeric_axiom_predicate(axiom);
        let mut rule_body = Vec::new();
        for part in &axiom.parts {
            if let crate::translate::function_expression::FunctionalExpression::Primitive(pne) = part {
                rule_body.push(get_defined_function_predicate(&pne.symbol, &pne.args));
            }
        }
        rules.push((rule_body.clone(), axiom_pred.clone()));
        
        // Rule 2: defined!axiom-head :- @axiom
        let head = axiom.get_head();
        let rule_2_head = get_defined_function_predicate(&head.symbol, &axiom.parameters);
        rules.push((vec![axiom_pred.clone()], rule_2_head));
        
        // Rule 3: For each primitive part, add fluent tracking rule
        // fluent-head :- @axiom, fluent!part
        let fluent_head_pred = bm::SymAtom::new(head.symbol.clone(), axiom.parameters.clone());
        for part in &axiom.parts {
            if let crate::translate::function_expression::FunctionalExpression::Primitive(pne) = part {
                let mut fluent_body = vec![axiom_pred.clone()];
                let fluent_part = bm::SymAtom::new(pne.symbol.clone(), pne.args.clone());
                fluent_body.push(fluent_part);
                rules.push((fluent_body, fluent_head_pred.clone()));
            }
        }
    }
    
    Ok(rules)
}

/// Get the axiom predicate atom for a numeric axiom
fn get_numeric_axiom_predicate(
    axiom: &crate::translate::normalization_function_admin::NumericAxiom,
) -> bm::SymAtom {
    let mut args = axiom.parameters.clone();
    for part in &axiom.parts {
        if let crate::translate::function_expression::FunctionalExpression::Primitive(pne) = part {
            args.extend(pne.args.clone());
        }
    }
    bm::SymAtom::new(axiom.name.clone(), args)
}

/// Get the action predicate atom for use in exploration rules.
/// Creates an atom like @action-move(?bot, ?from, ?to)
fn get_action_predicate(action: &TaskAction) -> bm::SymAtom {
    let predicate = format!("@action-{}", action.name);
    let args: Vec<String> = action
        .parameters
        .iter()
        .map(|(name, _type)| {
            if name.starts_with('?') {
                name.clone()
            } else {
                format!("?{}", name)
            }
        })
        .collect();
    bm::SymAtom::new(predicate, args)
}

/// Get the effect head atom for a conditional effect.
/// Returns None for negated atoms or unsupported effects.
fn get_effect_rule_heads_checked(effect: &TaskEffect) -> Result<Vec<bm::SymAtom>, String> {
    use crate::translate::pddl_parser::SExpr;

    match &effect.effect {
        SExpr::List(items) if !items.is_empty() => {
            if let SExpr::Atom(op) = &items[0] {
                if op == "not" {
                    return Ok(Vec::new());
                }
                if matches!(op.as_str(), "increase" | "decrease" | "assign" | "scale-up" | "scale-down") {
                    if items.len() >= 2 {
                        if let SExpr::List(func_items) = &items[1] {
                            if let Some(SExpr::Atom(func_name)) = func_items.get(0) {
                                let args: Vec<String> = func_items[1..]
                                    .iter()
                                    .filter_map(|item| {
                                        if let SExpr::Atom(s) = item {
                                            Some(s.clone())
                                        } else {
                                            None
                                        }
                                    })
                                    .collect();
                                return Ok(vec![
                                    get_defined_function_predicate(func_name, &args),
                                    bm::SymAtom::new(func_name.clone(), args),
                                ]);
                            }
                        }
                    }
                    return Err("numeric effect missing function head".to_string());
                }
            }

            if let SExpr::Atom(predicate) = &items[0] {
                let args: Vec<String> = items[1..]
                    .iter()
                    .filter_map(|item| {
                        if let SExpr::Atom(s) = item {
                            Some(s.clone())
                        } else {
                            None
                        }
                    })
                    .collect();
                return Ok(vec![bm::SymAtom::new(predicate.clone(), args)]);
            }
            Err("effect head is not an atom".to_string())
        }
        _ => Err("effect is not a non-empty list".to_string()),
    }
}

/// Get the axiom predicate atom for use in exploration rules.
/// Creates an atom using the axiom's name
fn get_axiom_predicate(axiom: &TaskAxiom) -> bm::SymAtom {
    let args: Vec<String> = axiom
        .parameters
        .iter()
        .map(|(name, _type)| {
            if name.starts_with('?') {
                name.clone()
            } else {
                format!("?{}", name)
            }
        })
        .collect();
    bm::SymAtom::new(axiom.name.clone(), args)
}

/// Convert a condition to a rule body (list of positive atoms).
/// Extracts parameter type predicates and positive literals from the condition.
fn condition_to_rule_body(
    parameters: &[(String, Option<String>)],
    condition: &Condition,
) -> Vec<bm::SymAtom> {
    let mut result = Vec::new();
    
    // Add type predicates for parameters
    for (param_name, param_type) in parameters {
        if let Some(type_name) = param_type {
            let param_var = if param_name.starts_with('?') {
                param_name.clone()
            } else {
                format!("?{}", param_name)
            };
            result.push(bm::SymAtom::new(type_name.clone(), vec![param_var]));
        }
    }
    
    // Extract positive literals from condition
    extract_positive_literals(condition, &mut result);
    
    result
}

/// Recursively extract positive (non-negated) literals from a condition.
fn extract_positive_literals(condition: &Condition, result: &mut Vec<bm::SymAtom>) {
    match condition {
        Condition::True => {
            // True condition adds no atoms
        }
        Condition::Atom(predicate, args) => {
            // Positive atom - add it
            result.push(bm::SymAtom::new(predicate.clone(), args.clone()));
        }
        Condition::And(conditions) => {
            // Conjunction - extract from all subconditions
            for cond in conditions {
                extract_positive_literals(cond, result);
            }
        }
        Condition::Or(_conditions) => {
            // Disjunction - after normalization, this shouldn't appear in simple preconditions
            // but if it does, we skip it (can't easily represent in datalog)
        }
        Condition::Not(cond) => {
            // Check if it's a negated atom (negative precondition)
            // In relaxed semantics, we ignore negative preconditions
            // But if it's not(not(X)), we should handle the inner part
            if let Condition::Not(inner) = cond.as_ref() {
                // Double negation - extract from inner
                extract_positive_literals(inner, result);
            }
            // Otherwise skip (negative literal)
        }
        Condition::Exists(params, cond) => {
            // Existential - add parameter types and extract from body
            for (param_name, param_type) in params {
                if let Some(type_name) = param_type {
                    let param_var = if param_name.starts_with('?') {
                        param_name.clone()
                    } else {
                        format!("?{}", param_name)
                    };
                    result.push(bm::SymAtom::new(type_name.clone(), vec![param_var]));
                }
            }
            extract_positive_literals(cond, result);
        }
        Condition::Forall(_params, _cond) => {
            // Universal quantifier - shouldn't appear after normalization
            // Skip if it does
        }
        Condition::Comparison(_op, left, right) => {
            add_defined_function_predicates_from_sexpr(left, result);
            add_defined_function_predicates_from_sexpr(right, result);
        }
    }
}

fn add_defined_function_predicates_from_sexpr(expr: &SExpr, result: &mut Vec<bm::SymAtom>) {
    use crate::translate::function_expression::parse_functional_expression;

    if let Some(func_expr) = parse_functional_expression(expr) {
        let mut primitives = Vec::new();
        collect_primitives(&func_expr, &mut primitives);
        for pne in primitives {
            result.push(get_defined_function_predicate(&pne.symbol, &pne.args));
        }
    }
}

fn collect_primitives(
    expr: &crate::translate::function_expression::FunctionalExpression,
    out: &mut Vec<crate::translate::function_expression::PrimitiveNumericExpression>,
) {
    use crate::translate::function_expression::FunctionalExpression;

    match expr {
        FunctionalExpression::Primitive(pne) => out.push(pne.clone()),
        FunctionalExpression::Arithmetic(ae) => {
            for part in &ae.parts {
                collect_primitives(part, out);
            }
        }
        FunctionalExpression::AdditiveInverse(ai) => collect_primitives(&ai.part, out),
        FunctionalExpression::Constant(_) => {}
    }
}

fn get_defined_function_predicate(symbol: &str, args: &[String]) -> bm::SymAtom {
    bm::SymAtom::new(format!("defined!{}", symbol), args.to_vec())
}
