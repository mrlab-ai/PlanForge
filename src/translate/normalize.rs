/// Axiom generated during universal quantifier removal.
#[derive(Debug, Clone)]
pub struct NormalizationAxiom {
    pub name: String,
    pub parameters: Vec<(String, Option<String>)>, // (name, type)
    pub condition: Condition,
}

/// Action with normalized condition for use during normalization.
#[derive(Debug, Clone)]
pub struct NormalizationAction {
    pub name: String,
    pub parameters: Vec<(String, Option<String>)>, // (name, type)
    pub precondition: Condition,
    // Note: effects are not included here as they're handled separately
}

/// Conditional effect with normalized condition for use during normalization.
/// Represents "when <condition> then <effect>"
#[derive(Debug, Clone)]
pub struct ConditionalEffect {
    pub parameters: Vec<(String, Option<String>)>, // (name, type) - forall parameters
    pub condition: Condition,
    // Note: the actual effect (add/delete/assign) is not included here
    // as we only need to normalize the condition
}

/// Context for managing axioms and type maps during normalization.
#[derive(Debug, Clone)]
pub struct NormalizationContext {
    pub axioms: Vec<NormalizationAxiom>,
    axiom_counter: usize,
}

impl NormalizationContext {
    pub fn new() -> Self {
        Self {
            axioms: Vec::new(),
            axiom_counter: 0,
        }
    }

    /// Create a new axiom with the given parameters and condition.
    /// Returns the axiom name.
    pub fn add_axiom(
        &mut self,
        parameters: Vec<(String, Option<String>)>,
        condition: Condition,
    ) -> String {
        let name = format!("new-axiom@{}", self.axiom_counter);
        self.axiom_counter += 1;
        self.axioms.push(NormalizationAxiom {
            name: name.clone(),
            parameters,
            condition,
        });
        name
    }
}

/// Remove universal quantifiers from a condition.
///
/// This function implements the normalization step [1] from Python's normalize.py:
/// Replace, in a top-down fashion, <forall(vars, phi)> by <not(not-all-phi)>,
/// where <not-all-phi> is a new axiom.
///
/// The negation of forall(vars, phi) becomes exists(vars, not(phi)),
/// and we create an axiom for that existential condition.
///
/// Note: Python version includes caching of axioms by condition. For simplicity,
/// we skip caching here since Condition contains SExpr which doesn't implement Hash.
/// This may create duplicate axioms but maintains functional correctness.
pub fn remove_universal_quantifiers(
    condition: &Condition,
    type_map: &HashMap<String, String>,
    ctx: &mut NormalizationContext,
) -> Condition {
    match condition {
        Condition::Forall(_params, _inner) => {
            // Negate to get exists(params, not(inner))
            // Note: negate() recursively negates, so forall becomes exists,
            // and nested foralls also get negated
            let axiom_condition = condition.negate();

            // Collect free variables (parameters for the new axiom)
            let mut free_vars: Vec<String> = axiom_condition.free_variables().into_iter().collect();
            free_vars.sort();

            // Create typed parameters from free variables
            let typed_params: Vec<(String, Option<String>)> = free_vars
                .iter()
                .map(|v| {
                    let ty = type_map.get(v).cloned();
                    (v.clone(), ty)
                })
                .collect();

            // Recursively process the axiom condition to handle any nested foralls
            // that might appear after negation
            let processed_condition = remove_universal_quantifiers(&axiom_condition, type_map, ctx);

            // Add the axiom
            let axiom_name = ctx.add_axiom(typed_params, processed_condition);

            // Return a negated atom referencing the axiom
            Condition::Not(Box::new(Condition::Atom(axiom_name, free_vars)))
        }
        // For Exists, recursively process the inner condition
        Condition::Exists(params, inner) => {
            let processed_inner = remove_universal_quantifiers(inner, type_map, ctx);
            Condition::Exists(params.clone(), Box::new(processed_inner))
        }
        // For all other conditions, recursively process sub-parts
        _ => {
            let parts = condition.parts();
            if parts.is_empty() {
                condition.clone()
            } else {
                let new_parts: Vec<Condition> = parts
                    .iter()
                    .map(|p| remove_universal_quantifiers(p, type_map, ctx))
                    .collect();
                condition.change_parts(new_parts)
            }
        }
    }
}


/// Substitute complicated goal conditions with axioms.
///
/// This function implements the goal normalization from Python's normalize.py.
/// If the goal is:
/// - A simple literal (Atom or Not(Atom)): leave as-is
/// - A conjunction of only literals: leave as-is
/// - Otherwise: create an axiom for the goal and replace it with an atom referencing that axiom
///
/// This simplifies goal handling in the rest of the translation pipeline.
pub fn substitute_complicated_goal(
    goal: &Condition,
    ctx: &mut NormalizationContext,
) -> Condition {
    // Check if goal is a simple literal (Atom or Not(Atom))
    match goal {
        Condition::Atom(_, _) => return goal.clone(),
        Condition::Not(inner) => {
            if matches!(**inner, Condition::Atom(_, _)) {
                return goal.clone();
            }
        }
        Condition::And(parts) => {
            // Check if all parts are literals
            let all_literals = parts.iter().all(|part| match part {
                Condition::Atom(_, _) => true,
                Condition::Not(inner) => matches!(**inner, Condition::Atom(_, _)),
                _ => false,
            });
            if all_literals {
                return goal.clone();
            }
        }
        _ => {}
    }

    // Goal is complicated - create an axiom for it
    let axiom_name = ctx.add_axiom(vec![], goal.clone());
    Condition::Atom(axiom_name, vec![])
}

/// Convert condition to Disjunctive Normal Form (DNF).
///
/// This function implements the DNF transformation from Python's normalize.py.
/// After removing universal quantifiers, the following rules are applied:
/// (1) or(phi, or(psi, psi'))      ==  or(phi, psi, psi')         [Associativity]
/// (2) exists(vars, or(phi, psi))  ==  or(exists(vars, phi), exists(vars, psi)) [Distribution]
/// (3) and(phi, or(psi, psi'))     ==  or(and(phi, psi), and(phi, psi'))        [Distribution]
///
/// This pulls all disjunctions to the outermost level of the condition.
pub fn build_dnf(condition: &Condition) -> Condition {
    // Recursively process all parts first
    let processed_parts: Vec<Condition> = condition
        .parts()
        .iter()
        .map(|part| build_dnf(part))
        .collect();

    // If no parts, return as-is
    if processed_parts.is_empty() {
        return condition.clone();
    }

    // Separate disjunctive parts from other parts
    let mut disjunctive_parts: Vec<Condition> = Vec::new();
    let mut other_parts: Vec<Condition> = Vec::new();

    for part in processed_parts {
        if matches!(part, Condition::Or(_)) {
            disjunctive_parts.push(part);
        } else {
            other_parts.push(part);
        }
    }

    // If no disjunctive parts, just reconstruct with processed parts
    if disjunctive_parts.is_empty() {
        let all_parts: Vec<Condition> = condition
            .parts()
            .iter()
            .map(|part| build_dnf(part))
            .collect();
        return condition.change_parts(all_parts);
    }

    // Apply transformation rules based on the condition type
    match condition {
        // Rule (1): Associativity of disjunction
        // or(phi, or(psi, psi')) → or(phi, psi, psi')
        Condition::Or(_) => {
            let mut result_parts = other_parts;
            for part in disjunctive_parts {
                if let Condition::Or(inner_parts) = part {
                    result_parts.extend(inner_parts);
                }
            }
            Condition::Or(result_parts)
        }

        // Rule (2): Distributivity disjunction/existential quantification
        // exists(vars, or(phi, psi)) → or(exists(vars, phi), exists(vars, psi))
        Condition::Exists(params, _) => {
            if let Some(Condition::Or(or_parts)) = disjunctive_parts.first() {
                let result_parts: Vec<Condition> = or_parts
                    .iter()
                    .map(|part| Condition::Exists(params.clone(), Box::new(part.clone())))
                    .collect();
                Condition::Or(result_parts)
            } else {
                // Fallback - shouldn't happen
                condition.change_parts(
                    other_parts
                        .into_iter()
                        .chain(disjunctive_parts)
                        .collect(),
                )
            }
        }

        // Rule (3): Distributivity disjunction/conjunction
        // and(phi, or(psi, psi')) → or(and(phi, psi), and(phi, psi'))
        Condition::And(_) => {
            // Start with conjunction of non-disjunctive parts
            let mut result_parts = vec![Condition::And(other_parts.clone())];

            // Distribute each disjunctive part
            while let Some(disj) = disjunctive_parts.pop() {
                let previous_result_parts = result_parts;
                result_parts = Vec::new();

                if let Condition::Or(parts_to_distribute) = disj {
                    for part1 in &previous_result_parts {
                        for part2 in &parts_to_distribute {
                            // Create conjunction of part1 and part2
                            let new_conj = match part1 {
                                Condition::And(conj_parts) => {
                                    let mut new_parts = conj_parts.clone();
                                    new_parts.push(part2.clone());
                                    Condition::And(new_parts)
                                }
                                _ => Condition::And(vec![part1.clone(), part2.clone()]),
                            };
                            result_parts.push(new_conj);
                        }
                    }
                }
            }

            Condition::Or(result_parts)
        }

        // For other condition types, just return with processed parts
        _ => condition.change_parts(
            other_parts
                .into_iter()
                .chain(disjunctive_parts)
                .collect(),
        ),
    }
}


/// Check if a condition is a top-level disjunction.
///
/// This is used to identify conditions that should be split.
pub fn is_disjunction(condition: &Condition) -> bool {
    matches!(condition, Condition::Or(_))
}

/// Split a disjunctive condition into its parts.
///
/// This function implements the split_disjunctions step from Python's normalize.py.
/// After build_DNF, conditions are in the form or(phi, psi, ...).
/// This step splits such conditions into multiple copies, one for each disjunct.
///
/// In the Python version, this operates on a task structure with proxies that can
/// clone actions/axioms/goals. In this Rust version, we provide the building block
/// that extracts the disjunctive parts that would be used to create separate entities.
///
/// For integration with a full task structure, each part returned by this function
/// would be used to create a copy of the original action/axiom/goal.
///
/// Returns:
/// - `Some(Vec<Condition>)` - The individual disjuncts if the condition is a disjunction
/// - `None` - If the condition is not a disjunction
pub fn split_disjunction(condition: &Condition) -> Option<Vec<Condition>> {
    match condition {
        Condition::Or(parts) => Some(parts.clone()),
        _ => None,
    }
}

fn simplify_condition(condition: &Condition) -> Condition {
    match condition {
        Condition::And(parts) => {
            let mut new_parts: Vec<Condition> = parts
                .iter()
                .map(simplify_condition)
                .filter(|p| !matches!(p, Condition::True))
                .collect();
            let mut flattened = Vec::new();
            for part in new_parts.drain(..) {
                if let Condition::And(inner) = part {
                    flattened.extend(inner);
                } else {
                    flattened.push(part);
                }
            }
            match flattened.len() {
                0 => Condition::True,
                1 => flattened.into_iter().next().unwrap(),
                _ => Condition::And(flattened),
            }
        }
        Condition::Or(parts) => {
            let mut new_parts: Vec<Condition> = parts
                .iter()
                .map(simplify_condition)
                .collect();
            if new_parts.iter().any(|p| matches!(p, Condition::True)) {
                return Condition::True;
            }
            let mut flattened = Vec::new();
            for part in new_parts.drain(..) {
                if let Condition::Or(inner) = part {
                    flattened.extend(inner);
                } else {
                    flattened.push(part);
                }
            }
            match flattened.len() {
                0 => Condition::True,
                1 => flattened.into_iter().next().unwrap(),
                _ => Condition::Or(flattened),
            }
        }
        Condition::Not(inner) => Condition::Not(Box::new(simplify_condition(inner))),
        Condition::Exists(params, inner) => {
            Condition::Exists(params.clone(), Box::new(simplify_condition(inner)))
        }
        Condition::Forall(params, inner) => {
            Condition::Forall(params.clone(), Box::new(simplify_condition(inner)))
        }
        _ => condition.clone(),
    }
}

/// Move existential quantifiers out of conjunctions and group them.
///
/// This function implements the quantifier movement step from Python's normalize.py.
/// After removing universal quantifiers and creating the disjunctive form,
/// the following rules are applied:
/// (1) exists(vars, exists(vars', phi))  ==  exists(vars + vars', phi)
/// (2) and(phi, exists(vars, psi))       ==  exists(vars, and(phi, psi)),
///       if var does not occur in phi as a free variable.
///
/// This pulls existential quantifiers outward and combines them, preparing
/// for the elimination step that follows.
pub fn move_existential_quantifiers(condition: &Condition) -> Condition {
    // Recursively process all parts first
    let processed_parts: Vec<Condition> = condition
        .parts()
        .iter()
        .map(|p| move_existential_quantifiers(p))
        .collect();

    // If no parts, return as-is
    if processed_parts.is_empty() {
        return condition.clone();
    }

    // Separate existential parts from other parts
    let mut existential_parts: Vec<Condition> = Vec::new();
    let mut other_parts: Vec<Condition> = Vec::new();

    for part in processed_parts {
        if matches!(part, Condition::Exists(_, _)) {
            existential_parts.push(part);
        } else {
            other_parts.push(part);
        }
    }

    // If no existential parts, just reconstruct with processed parts
    if existential_parts.is_empty() {
        return condition.change_parts(other_parts);
    }

    // Apply transformation rules based on the condition type
    match condition {
        // Rule (1): Combine nested quantifiers
        // exists(vars, exists(vars', phi)) → exists(vars + vars', phi)
        Condition::Exists(params, _) => {
            if let Some(Condition::Exists(inner_params, inner_cond)) = existential_parts.first() {
                // Combine parameters from outer and inner exists
                let mut new_parameters = params.clone();
                new_parameters.extend(inner_params.clone());
                
                // The new condition is the inner condition's parts
                // (which should be a single condition)
                Condition::Exists(new_parameters, inner_cond.clone())
            } else {
                // No nested exists, return as-is
                condition.change_parts({
                    let mut all = other_parts;
                    all.extend(existential_parts);
                    all
                })
            }
        }

        // Rule (2): Pull quantifiers out of conjunctions
        // and(phi, exists(vars, psi)) → exists(vars, and(phi, psi))
        Condition::And(_) => {
            // Collect all parameters from existential parts
            let mut new_parameters: Vec<(String, Option<String>)> = Vec::new();
            let mut new_conjunction_parts = other_parts;
            
            for part in existential_parts {
                if let Condition::Exists(params, inner) = part {
                    new_parameters.extend(params);
                    // Extract the parts from the inner condition
                    // If it's a conjunction, add all its parts
                    // Otherwise, add the condition itself
                    match *inner {
                        Condition::And(inner_parts) => {
                            new_conjunction_parts.extend(inner_parts);
                        }
                        other => {
                            new_conjunction_parts.push(other);
                        }
                    }
                }
            }
            
            let new_conjunction = Condition::And(new_conjunction_parts);
            Condition::Exists(new_parameters, Box::new(new_conjunction))
        }

        // For other condition types, just reconstruct with processed parts
        _ => condition.change_parts({
            let mut all = other_parts;
            all.extend(existential_parts);
            all
        }),
    }
}


/// Eliminate existential quantifiers from axioms by turning them into parameters.
///
/// This function implements normalization step [5a] from Python's normalize.py.
/// After move_existential_quantifiers, axiom conditions may be in the form:
/// `exists(?x, ?y, phi)`. This step converts them to axiom parameters:
///
/// Before: axiom(a, b) with condition exists(?x, ?y, phi(?x, ?y, a, b))
/// After:  axiom(a, b, ?x, ?y) with condition phi(?x, ?y, a, b)
///
/// This is done in-place by modifying the axiom's parameters and condition.
/// The existential quantifiers are "dropped" by moving their parameters to
/// the axiom's parameter list.
pub fn eliminate_existential_quantifiers_from_axioms(
    axioms: &mut [NormalizationAxiom],
) {
    for axiom in axioms.iter_mut() {
        if let Condition::Exists(params, inner) = &axiom.condition {
            // Extend the axiom's parameters with the existential parameters
            axiom.parameters.extend(params.clone());
            
            // Replace the condition with the inner condition (first part)
            // In Python, precond.parts[0] is used, and parts is a tuple with one element
            axiom.condition = (**inner).clone();
        }
    }
}


/// Eliminate existential quantifiers from action preconditions by turning them into parameters.
///
/// This function implements normalization step [5b] from Python's normalize.py.
/// After move_existential_quantifiers, action preconditions may be in the form:
/// `exists(?x, ?y, phi)`. This step converts them to action parameters:
///
/// Before: action(a, b) with precondition exists(?x, ?y, phi(?x, ?y, a, b))
/// After:  action(a, b, ?x, ?y) with precondition phi(?x, ?y, a, b)
///
/// Note: The additional parameters don't form part of the action name - they're
/// used during grounding but not for action identification.
///
/// This is done in-place by modifying the action's parameters and precondition.
/// The existential quantifiers are "dropped" by moving their parameters to
/// the action's parameter list.
pub fn eliminate_existential_quantifiers_from_preconditions(
    actions: &mut [NormalizationAction],
) {
    for action in actions.iter_mut() {
        if let Condition::Exists(params, inner) = &action.precondition {
            // Extend the action's parameters with the existential parameters
            action.parameters.extend(params.clone());
            
            // Replace the precondition with the inner condition (first part)
            // In Python, precond.parts[0] is used, and parts is a tuple with one element
            action.precondition = (**inner).clone();
        }
    }
}


/// Eliminate existential quantifiers from conditional effect conditions.
///
/// This function implements normalization step [5c] from Python's normalize.py.
/// For effect conditions, we replace "when exists(x, phi) then e" with
/// "forall(x): when phi then e".
///
/// This is done by moving the existential parameters to the effect's parameter list.
/// The existential becomes a universal quantifier over the effect.
///
/// Before: effect with condition exists(?x, ?y, phi(?x, ?y)) and parameters []
/// After:  effect with condition phi(?x, ?y) and parameters [?x, ?y]
///
/// Note: The effect parameters represent forall quantification over the effect,
/// not existential quantification. The transformation converts the existential
/// in the condition to universal quantification over the entire conditional effect.
pub fn eliminate_existential_quantifiers_from_conditional_effects(
    effects: &mut [ConditionalEffect],
) {
    for effect in effects.iter_mut() {
        if let Condition::Exists(params, inner) = &effect.condition {
            // Extend the effect's parameters with the existential parameters
            effect.parameters.extend(params.clone());
            
            // Replace the condition with the inner condition (first part)
            effect.condition = (**inner).clone();
        }
    }
}

/// Verify and fix arithmetic expressions in the task.
///
/// This function implements verification from Python's normalize.py.
/// It validates that all function symbols used in numeric expressions are properly
/// defined in the domain. If undefined functions are found, it adds them to the
/// domain with default initialization.
///
/// From Python's normalize.py:
/// - Collects all defined function names from task.functions
/// - Recursively validates expressions in:
///   * Action effect assignments (peffect.expression)
///   * Metric expression (task.metric)
/// - For each PrimitiveNumericExpression found:
///   * If function symbol not in function_names:
///     - Print warning to stderr
///     - Add new Function with arity matching the expression
///     - For 0-arity functions: add to functions list and initialize to 0.0
///     - For higher-arity: raise error (would need parameter types)
/// - For ArithmeticExpressions: recursively verify all parts
/// - For AdditiveInverse: verify the single part
///
/// Note: This implementation works with SExpr-based effects. A full implementation
/// would require typed NumericExpression structures, but this provides basic validation
/// for function symbols found in effect expressions.
pub fn task_verify_and_fix_arithmetic_expressions(task: &mut NormalizableTask) {
    use std::collections::HashSet;
    
    // Collect all defined function names
    let mut function_names: HashSet<String> = task.functions.iter().map(|(name, _)| name.clone()).collect();
    
    // Helper to recursively scan SExpr for function symbols
    fn find_function_symbols(expr: &SExpr, symbols: &mut HashSet<String>) {
        match expr {
            SExpr::Atom(_) => {}, // Constants or variables, not functions
            SExpr::List(items) => {
                if items.is_empty() {
                    return;
                }
                // First element might be a function/operator
                if let SExpr::Atom(op) = &items[0] {
                    // Check if it looks like a function call (not a standard operator)
                    // Standard operators: +, -, *, /, =, <, >, <=, >=, etc.
                    if !matches!(op.as_str(), "+" | "-" | "*" | "/" | "=" | "<" | ">" | "<=" | ">=" | "and" | "or" | "not" | "increase" | "decrease" | "assign") {
                        // This might be a function symbol
                        symbols.insert(op.clone());
                    }
                }
                // Recursively check all sub-expressions
                for item in items {
                    find_function_symbols(item, symbols);
                }
            }
        }
    }
    
    // Collect all function symbols used in numeric effects (not predicate effects)
    let mut used_symbols = HashSet::new();
    for action in &task.actions {
        for effect in &action.effects {
            // Only check numeric effects (increase, decrease, assign)
            // Skip regular predicate effects like (at ?i ?x) or (free ?a)
            if let SExpr::List(items) = &effect.effect {
                if !items.is_empty() {
                    if let SExpr::Atom(op) = &items[0] {
                        // Only scan numeric operators
                        if matches!(op.as_str(), "increase" | "decrease" | "assign" | "scale-up" | "scale-down") {
                            find_function_symbols(&effect.effect, &mut used_symbols);
                        }
                        // Skip predicate effects (anything else)
                    }
                }
            }
        }
    }

    // Collect symbols from metric expression (if any)
    if let Some(metric_expr) = &task.metric.1 {
        find_function_symbols(metric_expr, &mut used_symbols);
    }
    
    // Check for undefined functions
    let mut undefined_functions = Vec::new();
    for symbol in &used_symbols {
        if !function_names.contains(symbol) {
            undefined_functions.push(symbol.clone());
        }
    }
    
    // Add undefined 0-arity functions with warning
    for symbol in undefined_functions {
        eprintln!(
            "WARNING: Function symbol '{}' appears in a numeric expression but is not defined in domain file.",
            symbol
        );
        eprintln!("Adding new symbol '{}' with arity 0.", symbol);
        
        // Add to functions list
        task.functions.push((symbol.clone(), vec![]));
        function_names.insert(symbol.clone());
        
        // Note: Initial value assignment would be added to task.init here
        // Format: (= (symbol) 0)
        // For now, we just add the function definition
        let init_expr = SExpr::List(vec![
            SExpr::Atom("=".to_string()),
            SExpr::List(vec![SExpr::Atom(symbol)]),
            SExpr::Atom("0".to_string()),
        ]);
        task.init.push(init_expr);
    }
}

/// Remove arithmetic expressions from the task.
///
/// This function implements normalization step [10] from Python's normalize.py.
/// It replaces complex arithmetic expressions with references to derived functions.
///
/// From Python's normalize.py:
/// ```python
/// def remove_arithmetic_expressions(task):
///     def recurse(condition):
///         if isinstance(condition, pddl.FunctionComparison):
///             parts = [task.function_administrator.get_derived_function(exp)
///                         for exp in condition.parts]
///             if condition.negated:
///                 return pddl.NegatedFunctionComparison(condition.comparator,parts)
///             else:
///                 return pddl.FunctionComparison(condition.comparator,parts)
///         else:
///             new_parts = [recurse(part) for part in condition.parts]
///             return condition.change_parts(new_parts)
///
///     # remove from conditions
///     for proxy in tuple(all_conditions(task)):
///         proxy.set(recurse(proxy.condition))
///
///     # remove from actions
///     admin = task.function_administrator
///     for act in task.actions:
///         for eff in act.effects:
///             if isinstance(eff.peffect,pddl.FunctionAssignment):
///                 assign = eff.peffect
///                 assign.expression = admin.get_derived_function(assign.expression)
///     
///     # remove from metric expression 
///     opt_dir, metric_expression = task.metric
///     if metric_expression is None:
///         assert opt_dir == '<'
///         metric_expression = -1
///     else:
///         metric_expression = admin.get_derived_function(metric_expression)
///     task.metric = (opt_dir, metric_expression)
/// ```
pub fn task_remove_arithmetic_expressions(task: &mut NormalizableTask) {
    use crate::translate::function_expression::parse_functional_expression;
    use crate::translate::normalization_function_admin::pne_to_sexpr;
    
    // Helper to process comparison conditions and replace arithmetic expressions
    fn process_condition(
        cond: &Condition,
        admin: &mut crate::translate::normalization_function_admin::NormalizationFunctionAdministrator,
    ) -> Condition {
        match cond {
            Condition::Comparison(comparator, left, right) => {
                // Parse both sides as functional expressions
                let left_result = if let Some(left_expr) = parse_functional_expression(left) {
                    // Process through function administrator
                    let pne = admin.get_derived_function(&left_expr);
                    pne_to_sexpr(&pne)
                } else {
                    // If parsing fails, keep original
                    left.clone()
                };
                
                let right_result = if let Some(right_expr) = parse_functional_expression(right) {
                    // Process through function administrator
                    let pne = admin.get_derived_function(&right_expr);
                    pne_to_sexpr(&pne)
                } else {
                    // If parsing fails, keep original
                    right.clone()
                };
                
                Condition::Comparison(comparator.clone(), left_result, right_result)
            }
            Condition::And(parts) => {
                Condition::And(parts.iter().map(|p| process_condition(p, admin)).collect())
            }
            Condition::Or(parts) => {
                Condition::Or(parts.iter().map(|p| process_condition(p, admin)).collect())
            }
            Condition::Not(inner) => {
                Condition::Not(Box::new(process_condition(inner, admin)))
            }
            Condition::Exists(params, inner) => {
                Condition::Exists(params.clone(), Box::new(process_condition(inner, admin)))
            }
            Condition::Forall(params, inner) => {
                Condition::Forall(params.clone(), Box::new(process_condition(inner, admin)))
            }
            // Atoms and True are unchanged
            _ => cond.clone(),
        }
    }
    
    // Helper to process effect expressions
    fn process_effect_expr(
        effect_sexpr: &SExpr,
        admin: &mut crate::translate::normalization_function_admin::NormalizationFunctionAdministrator,
    ) -> SExpr {
        match effect_sexpr {
            SExpr::List(items) if items.len() >= 3 => {
                // Check if this is an assignment-like effect
                if let SExpr::Atom(op) = &items[0] {
                    if matches!(op.as_str(), "assign" | "increase" | "decrease" | "scale-up" | "scale-down") {
                        // Pattern: (op fluent expression)
                        // Process the expression (items[2..])
                        let fluent = items[1].clone();
                        
                        // Build expression SExpr from remaining items
                        let expr_sexpr = if items.len() == 3 {
                            items[2].clone()
                        } else {
                            // Multiple items, wrap in a list if needed
                            items[2].clone()
                        };
                        
                        // Try to parse and process
                        let processed_expr = if let Some(func_expr) = parse_functional_expression(&expr_sexpr) {
                            let pne = admin.get_derived_function(&func_expr);
                            pne_to_sexpr(&pne)
                        } else {
                            expr_sexpr
                        };
                        
                        // Reconstruct effect
                        return SExpr::List(vec![
                            SExpr::Atom(op.clone()),
                            fluent,
                            processed_expr,
                        ]);
                    }
                }
                effect_sexpr.clone()
            }
            _ => effect_sexpr.clone(),
        }
    }
    
    // Process all action preconditions
    for i in 0..task.actions.len() {
        let precond = task.actions[i].precondition.clone();
        task.actions[i].precondition = process_condition(&precond, &mut task.function_administrator);
    }
    
    // Process all effect conditions
    for i in 0..task.actions.len() {
        for j in 0..task.actions[i].effects.len() {
            let cond = task.actions[i].effects[j].condition.clone();
            task.actions[i].effects[j].condition = process_condition(&cond, &mut task.function_administrator);
            
            // Also process the effect expression itself
            let effect_expr = task.actions[i].effects[j].effect.clone();
            task.actions[i].effects[j].effect = process_effect_expr(&effect_expr, &mut task.function_administrator);
        }
    }
    
    // Process all axiom conditions
    for i in 0..task.axioms.len() {
        let cond = task.axioms[i].condition.clone();
        task.axioms[i].condition = process_condition(&cond, &mut task.function_administrator);
    }
    
    // Process goal
    let goal = task.goal.clone();
    task.goal = process_condition(&goal, &mut task.function_administrator);
    
    // Collect all generated numeric axioms
    let numeric_axioms = task.function_administrator.get_all_axioms();
    task.numeric_axioms = numeric_axioms;
    
    // Process metric expression
    let metric_expr = task.metric.1.clone();
    let new_metric_expr = if let Some(expr) = metric_expr {
        if let Some(func_expr) = parse_functional_expression(&expr) {
            let pne = task.function_administrator.get_derived_function(&func_expr);
            Some(pne_to_sexpr(&pne))
        } else {
            Some(expr)
        }
    } else {
        Some(SExpr::Atom("-1".to_string()))
    };
    task.metric.1 = new_metric_expr;
}

/// Verify that derived predicates are not used in :init or action effects.
///
/// This function implements normalization step [11] from Python's normalize.py.
/// It validates that axiom predicates (derived predicates) do not appear in:
/// - The initial state (:init)
/// - Action effects
///
/// Derived predicates should only appear in axiom heads and in conditions.
/// If a derived predicate appears in :init or effects, the task is invalid.
///
/// From Python's normalize.py:
/// ```python
/// def verify_axiom_predicates(task):
///     axiom_names = set()
///     for axiom in task.axioms:
///         axiom_names.add(axiom.name)
///
///     for fact in task.init:
///         if getattr(fact, "predicate", None) in axiom_names:
///             raise SystemExit("error: derived predicate %r appears in :init fact '%s'" %
///                 (fact.predicate, fact))
///
///     for action in task.actions:
///         for effect in action.effects:
///             if isinstance(effect.peffect, pddl.Literal) and effect.peffect.predicate in axiom_names:
///                 raise SystemExit("error: derived predicate %r appears in effect of action %r" %
///                     (effect.peffect.predicate, action.name))
/// ```
pub fn task_verify_axiom_predicates(task: &NormalizableTask) -> Result<(), String> {
    use std::collections::HashSet;
    
    // Collect all axiom predicate names
    let axiom_names: HashSet<String> = task.axioms.iter().map(|ax| ax.name.clone()).collect();
    
    // Check that derived predicates don't appear in :init
    for init_fact in &task.init {
        // Extract predicate name from init fact
        // Init facts can be atoms like (at robot loc1) or assignments like (= (fuel) 10)
        if let Some(predicate) = extract_predicate_from_sexpr(init_fact) {
            if axiom_names.contains(&predicate) {
                return Err(format!(
                    "error: derived predicate '{}' appears in :init fact '{:?}'",
                    predicate, init_fact
                ));
            }
        }
    }
    
    // Check that derived predicates don't appear in action effects
    for action in &task.actions {
        for effect in &action.effects {
            // Extract predicate from effect
            // Effects can be atoms, negated atoms, or assignments
            if let Some(predicate) = extract_predicate_from_sexpr(&effect.effect) {
                if axiom_names.contains(&predicate) {
                    return Err(format!(
                        "error: derived predicate '{}' appears in effect of action '{}'",
                        predicate, action.name
                    ));
                }
            }
        }
    }
    
    Ok(())
}

/// Helper function to extract predicate name from an SExpr.
///
/// Handles various forms:
/// - (at robot loc1) -> "at"
/// - (not (clear block)) -> "clear"
/// - (= (fuel) 10) -> None (assignment, not a predicate)
fn extract_predicate_from_sexpr(expr: &SExpr) -> Option<String> {
    match expr {
        SExpr::Atom(_) => None,
        SExpr::List(items) => {
            if items.is_empty() {
                return None;
            }
            
            // Check first element
            match &items[0] {
                SExpr::Atom(op) => {
                    match op.as_str() {
                        // Negation: (not <atom>)
                        "not" => {
                            if items.len() >= 2 {
                                extract_predicate_from_sexpr(&items[1])
                            } else {
                                None
                            }
                        }
                        // Assignment operators: (= ...), (assign ...), etc.
                        "=" | "assign" | "increase" | "decrease" | "scale-up" | "scale-down" => None,
                        // Regular predicate
                        _ => Some(op.clone()),
                    }
                }
                _ => None,
            }
        }
    }
}

