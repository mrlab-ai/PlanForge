use crate::translate::build_model as bm;
use crate::translate::normalization_function_admin::{NormalizationFunctionAdministrator, NumericAxiom};
use crate::translate::pddl_ast::{Action, Condition, Domain, Problem};
use crate::translate::pddl_parser::SExpr;
use std::collections::{HashMap, HashSet};

const OBJECT_PREDICATE: &str = "@object";

/// A PDDL task structure suitable for normalization.
/// This contains the parsed domain and problem in a form where conditions
/// are already converted from SExpr to Condition, and actions/axioms can be
/// mutated during the normalization process.
#[derive(Debug, Clone)]
pub struct NormalizableTask {
    /// Domain name
    pub domain_name: String,
    /// Problem name  
    pub problem_name: String,
    /// Predicate definitions from domain
    pub predicates: Vec<(String, Vec<(String, Option<String>)>)>,
    /// Function (numeric fluent) definitions from domain
    pub functions: Vec<(String, Vec<(String, Option<String>)>)>,
    /// Actions with parsed preconditions (as Condition) and raw effects (as SExpr for now)
    pub actions: Vec<TaskAction>,
    /// Axioms generated during normalization
    pub axioms: Vec<TaskAxiom>,
    /// Numeric axioms generated by the function administrator
    pub numeric_axioms: Vec<NumericAxiom>,
    /// Objects from problem
    pub objects: Vec<(String, Option<String>)>,
    /// Initial state atoms (as SExpr for now)
    pub init: Vec<SExpr>,
    /// Goal condition (parsed as Condition)
    pub goal: Condition,
    /// Counter for generating unique axiom names
    axiom_counter: usize,
    /// Function administrator for managing derived numeric functions (not Clone)
    /// Note: This field makes NormalizableTask non-Clone, but we add a custom Clone impl
    #[allow(dead_code)]
    function_administrator: NormalizationFunctionAdministrator,
}

/// Action in the normalizable task with parsed precondition
#[derive(Debug, Clone)]
pub struct TaskAction {
    pub name: String,
    pub parameters: Vec<(String, Option<String>)>,
    pub precondition: Condition,
    /// Effects as conditional effects with parsed conditions
    pub effects: Vec<TaskEffect>,
    /// Cost effect (the increase of total-cost or cost fluent)
    /// In Python, this is extracted but also kept in effects list for backward compatibility
    pub cost: Option<SExpr>,
}

/// Conditional effect with parsed condition
#[derive(Debug, Clone)]
pub struct TaskEffect {
    /// Forall parameters for the effect
    pub parameters: Vec<(String, Option<String>)>,
    /// Condition for when the effect applies
    pub condition: Condition,
    /// The actual effect (add/delete/assign) - kept as SExpr for now
    pub effect: SExpr,
}

/// Axiom in the normalizable task
#[derive(Debug, Clone)]
pub struct TaskAxiom {
    pub name: String,
    pub parameters: Vec<(String, Option<String>)>,
    pub condition: Condition,
    /// Number of external parameters (used for rule generation)
    pub num_external_parameters: usize,
}

impl NormalizableTask {
    /// Create a normalizable task from parsed Domain and Problem ASTs
    pub fn from_ast(domain: &Domain, problem: &Problem) -> Self {
        use crate::translate::pddl_ast::sexpr_to_condition;
        
        // Convert actions: parse preconditions and effects from SExpr
        let actions: Vec<TaskAction> = domain
            .actions
            .iter()
            .map(|action| {
                let precondition = action
                    .precond
                    .as_ref()
                    .map(|s| sexpr_to_condition(s))
                    .unwrap_or(Condition::True);
                
                // Parse effects: convert conditional effects to TaskEffect structures
                let (mut effects, mut cost) = if let Some(effect_sexpr) = &action.effect {
                    Self::parse_effects_and_extract_cost(effect_sexpr)
                } else {
                    (vec![], None)
                };
                
                // Python compatibility: If no cost effect was extracted, add artificial (increase total-cost 1)
                // This matches Python's behavior in parsing_functions.py line 397
                if cost.is_none() && !effects.is_empty() {
                    let artificial_cost = SExpr::List(vec![
                        SExpr::Atom("increase".to_string()),
                        SExpr::List(vec![SExpr::Atom("total-cost".to_string())]),
                        SExpr::Atom("1".to_string()),
                    ]);
                    
                    // Add the artificial cost effect to the effects list
                    effects.push(TaskEffect {
                        parameters: vec![],
                        condition: Condition::True,
                        effect: artificial_cost.clone(),
                    });
                    
                    // Set it as the cost
                    cost = Some(artificial_cost);
                }
                
                TaskAction {
                    name: action.name.clone(),
                    parameters: action.parameters.clone(),
                    precondition,
                    effects,
                    cost,
                }
            })
            .collect();
        
        // Convert goal from SExpr to Condition
        let goal = problem
            .goal
            .as_ref()
            .map(|s| sexpr_to_condition(s))
            .unwrap_or(Condition::True);
        
        NormalizableTask {
            domain_name: domain.name.clone(),
            problem_name: problem.name.clone(),
            predicates: domain.predicates.clone(),
            functions: domain.functions.clone(),
            actions,
            axioms: vec![],
            numeric_axioms: vec![],
            objects: problem.objects.clone(),
            init: problem.init.clone(),
            goal,
            axiom_counter: 0,
            function_administrator: NormalizationFunctionAdministrator::new(),
        }
    }
    
    /// Parse effects from an SExpr into a list of TaskEffect structures.
    /// Handles conditional effects with forall, when, and nested structures.
    fn parse_effects(effect_sexpr: &SExpr) -> Vec<TaskEffect> {
        use crate::translate::pddl_ast::sexpr_to_condition;
        
        // Helper to parse parameters from typed list
        fn parse_params(params_sexpr: &SExpr) -> Vec<(String, Option<String>)> {
            match params_sexpr {
                SExpr::List(items) => {
                    let mut result = vec![];
                    let mut i = 0;
                    let mut pending_names = vec![];
                    
                    while i < items.len() {
                        if let SExpr::Atom(atom) = &items[i] {
                            if atom == "-" {
                                // Next item is the type
                                if i + 1 < items.len() {
                                    if let SExpr::Atom(type_name) = &items[i + 1] {
                                        // Assign type to all pending names
                                        for name in pending_names.drain(..) {
                                            result.push((name, Some(type_name.clone())));
                                        }
                                        i += 2;
                                        continue;
                                    }
                                }
                            } else {
                                // Variable name
                                pending_names.push(atom.clone());
                            }
                        }
                        i += 1;
                    }
                    
                    // Any remaining names without types
                    for name in pending_names {
                        result.push((name, None));
                    }
                    
                    result
                }
                _ => vec![],
            }
        }
        
        fn parse_effect_helper(s: &SExpr) -> Vec<TaskEffect> {
            match s {
                SExpr::List(items) if !items.is_empty() => {
                    if let SExpr::Atom(keyword) = &items[0] {
                        match keyword.to_lowercase().as_str() {
                            "and" => {
                                // (and effect1 effect2 ...) - flatten all effects
                                let mut all_effects = vec![];
                                for item in &items[1..] {
                                    all_effects.extend(parse_effect_helper(item));
                                }
                                all_effects
                            }
                            "forall" => {
                                // (forall (?v - type) effect) - effect with parameters
                                if items.len() >= 3 {
                                    let params = parse_params(&items[1]);
                                    let inner_effects = parse_effect_helper(&items[2]);
                                    // Add parameters to each inner effect
                                    inner_effects.into_iter().map(|mut eff| {
                                        eff.parameters.extend(params.clone());
                                        eff
                                    }).collect()
                                } else {
                                    vec![]
                                }
                            }
                            "when" => {
                                // (when condition effect) - conditional effect
                                if items.len() >= 3 {
                                    let cond = sexpr_to_condition(&items[1]);
                                    let inner_effects = parse_effect_helper(&items[2]);
                                    // Add condition to each inner effect
                                    inner_effects.into_iter().map(|mut eff| {
                                        // Combine conditions if effect already has one
                                        eff.condition = if matches!(eff.condition, Condition::True) {
                                            cond.clone()
                                        } else {
                                            Condition::And(vec![cond.clone(), eff.condition.clone()])
                                        };
                                        eff
                                    }).collect()
                                } else {
                                    vec![]
                                }
                            }
                            _ => {
                                // Simple effect (add, del, increase, decrease, assign, etc.)
                                vec![TaskEffect {
                                    parameters: vec![],
                                    condition: Condition::True,
                                    effect: s.clone(),
                                }]
                            }
                        }
                    } else {
                        // Unexpected structure, treat as simple effect
                        vec![TaskEffect {
                            parameters: vec![],
                            condition: Condition::True,
                            effect: s.clone(),
                        }]
                    }
                }
                _ => {
                    // Atom or empty list - treat as simple effect
                    vec![TaskEffect {
                        parameters: vec![],
                        condition: Condition::True,
                        effect: s.clone(),
                    }]
                }
            }
        }
        
        parse_effect_helper(effect_sexpr)
    }
    
    /// Check if an effect SExpr is a cost effect (increase total-cost only)
    /// Python only recognizes "total-cost" as a cost fluent, not "cost"
    /// (See f_expression.py:is_cost_assignment() line 257)
    fn is_cost_effect(effect: &SExpr) -> bool {
        match effect {
            SExpr::List(items) if items.len() >= 2 => {
                // Check for (increase (total-cost) ...) ONLY
                // Note: (increase (cost) ...) is NOT considered a cost effect in Python!
                if let SExpr::Atom(op) = &items[0] {
                    if op == "increase" {
                        if let SExpr::List(fluent_items) = &items[1] {
                            if !fluent_items.is_empty() {
                                if let SExpr::Atom(fluent_name) = &fluent_items[0] {
                                    return fluent_name == "total-cost";  // Only total-cost!
                                }
                            }
                        } else if let SExpr::Atom(fluent_name) = &items[1] {
                            return fluent_name == "total-cost";  // Only total-cost!
                        }
                    }
                }
                false
            }
            _ => false,
        }
    }
    
    /// Parse effects and extract cost effect.
    /// Like Python's parse_effects + extract_cost, the cost effect is kept in the effects list
    /// (for backward compatibility) but also returned separately.
    fn parse_effects_and_extract_cost(effect_sexpr: &SExpr) -> (Vec<TaskEffect>, Option<SExpr>) {
        let effects = Self::parse_effects(effect_sexpr);
        
        // Find cost effect (should be at most one)
        let cost = effects.iter()
            .find(|eff| Self::is_cost_effect(&eff.effect))
            .map(|eff| eff.effect.clone());
        
        // Return effects unchanged (includes cost) and cost separately
        // This matches Python's behavior: cost is in both places
        (effects, cost)
    }
    
    /// Add a new axiom to the task and return its name
    pub fn add_axiom(&mut self, parameters: Vec<(String, Option<String>)>, condition: Condition) -> String {
        let axiom_name = format!("@axiom-{}", self.axiom_counter);
        self.axiom_counter += 1;
        
        self.axioms.push(TaskAxiom {
            name: axiom_name.clone(),
            parameters: parameters.clone(),
            condition,
            num_external_parameters: parameters.len(),
        });
        
        axiom_name
    }
    
    /// Get a type map for a given set of parameters
    /// This is used when creating axioms to track variable types
    pub fn build_type_map(parameters: &[(String, Option<String>)]) -> HashMap<String, String> {
        parameters
            .iter()
            .filter_map(|(name, typ)| typ.as_ref().map(|t| (name.clone(), t.clone())))
            .collect()
    }
}

#[derive(Default, Debug, PartialEq)]
pub struct NormalizationOutcome {
    pub new_facts: Vec<bm::Atom>,
    pub object_predicate_required: bool,
}

fn collect_condition_vars(rule: &bm::RuleSpec) -> HashSet<String> {
    let mut vars = HashSet::new();
    for cond in &rule.conditions {
        for arg in &cond.args {
            if arg.starts_with('?') {
                vars.insert(arg.clone());
            }
        }
    }
    vars
}

/// Adds `@object(?x)` conditions for variables that only occur in the rule head.
fn remove_free_effect_variables(rules: &mut [bm::RuleSpec]) -> bool {
    let mut inserted = false;
    for rule in rules.iter_mut() {
        let mut bound_vars = collect_condition_vars(rule);
        let mut extra_conditions: Vec<bm::SymAtom> = Vec::new();
        for arg in &rule.effect.args {
            if !arg.starts_with('?') {
                continue;
            }
            if bound_vars.contains(arg) {
                continue;
            }
            let already_present = rule
                .conditions
                .iter()
                .any(|c| c.predicate == OBJECT_PREDICATE && c.args.len() == 1 && &c.args[0] == arg);
            if !already_present {
                extra_conditions.push(bm::SymAtom::new(
                    OBJECT_PREDICATE.to_string(),
                    vec![arg.clone()],
                ));
            }
            bound_vars.insert(arg.clone());
        }
        if !extra_conditions.is_empty() {
            rule.conditions.extend(extra_conditions);
            inserted = true;
        }
    }
    inserted
}

/// Deduplicates identical conditions within each rule to keep the join queue small.
fn split_duplicate_arguments(rules: &mut [bm::RuleSpec]) {
    for rule in rules.iter_mut() {
        let mut seen: HashSet<(String, Vec<String>)> = HashSet::new();
        rule.conditions.retain(|cond| {
            let key = (cond.predicate.clone(), cond.args.clone());
            seen.insert(key)
        });
    }
}

/// Converts rules without conditions (and constant heads) into base facts.
fn convert_trivial_rules(rules: &mut Vec<bm::RuleSpec>) -> Vec<bm::Atom> {
    let mut produced: Vec<bm::Atom> = Vec::new();
    rules.retain(|rule| {
        if rule.conditions.is_empty() {
            if rule.effect.args.iter().any(|a| a.starts_with('?')) {
                // Unable to convert – keep the rule around for later processing.
                true
            } else {
                produced.push(bm::Atom {
                    predicate: rule.effect.predicate.clone(),
                    args: rule
                        .effect
                        .args
                        .iter()
                        .map(|a| bm::Arg::Const(a.clone()))
                        .collect(),
                });
                false
            }
        } else {
            true
        }
    });
    produced
}

pub fn normalize_rules(rules: &mut Vec<bm::RuleSpec>) -> NormalizationOutcome {
    let mut outcome = NormalizationOutcome::default();
    outcome.object_predicate_required = remove_free_effect_variables(rules);
    split_duplicate_arguments(rules);
    outcome.new_facts = convert_trivial_rules(rules);
    outcome
}

